INSERT INTO concepts (id, name, ccgs_key, privilege_level, parent_level, domain_level, group_type_string, parent_key)
SELECT
  (SELECT COALESCE(MAX(id), 0) FROM concepts) + row_number() OVER (),
  t.name, t.ccgs_key, t.privilege_level, t.parent_level, t.domain_level, t.group_type_string, t.parent_key
FROM temp_ccgs_concepts t
WHERE NOT EXISTS (SELECT 1 FROM concepts WHERE name = t.name);

